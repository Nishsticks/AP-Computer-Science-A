public class BoxHouse_Stepp {
	public static void main(String[] args) { 
		buildRoof();
		buildBody();

	}

	public static void buildRoof()
	{
		System.out.println("   ++");
		System.out.println("  +  +");
		System.out.println(" +    +");

	}
	public static void buildBody()
	{
		System.out.println("+------+");
		System.out.println("|      |");
		System.out.println("|      |");
		System.out.println("+------+");
	}

}
