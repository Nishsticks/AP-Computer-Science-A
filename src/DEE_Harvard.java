public class DEE_Harvard {
	public static void main(String[] args) {
		System.out.println(" -----");
		drawD();
		System.out.println(" -----");
		System.out.println();
		drawE();
		System.out.println();
		drawE();
	}

	public static void drawD()
	{
		System.out.println(" | \\");
		System.out.println(" | |");
		System.out.println(" | /");

	}

	public static void drawE()
	{
		System.out.println(" +-----");
		System.out.println(" |");
		System.out.println(" +----");
		System.out.println(" |");
		System.out.println(" +-----");

	}

}